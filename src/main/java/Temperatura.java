
public class Temperatura {
    //ATRIBUTOS PRIVADOS
   private double graus;

   //CONSTRUTOR
   public Temperatura (double grausT){
       this.graus = grausT;
   }
   
   //GETTER
   public double getGraus(){
       return graus;
   }
   //SETTER
   public void setGraus(double novoGraUs){
       this.graus = novoGraUs; 
   }
}
